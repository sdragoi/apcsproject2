public class Book {
	
	public String title;
	public String author;
	public boolean isHardcover;
	public String genre;
	
	public Book (String title, String author, String genre, boolean isHardcover) {
		this.title = title;
		this.author = author;
		this.genre = genre;
		this.isHardcover = isHardcover;
	}

}
